
public class counting {

	public static void main(String[] args) {
		//begin counting
		for(int count=0;count!=100;count+=5 ) 
		{
		/*Counting by 5; prints the current count number, then the next 4 numbers. 
		Afterwards, increment the count value by 5.
		*/
			System.out.print("  ");
			System.out.print(count+1); 
			System.out.print("  ");
			System.out.print(count+2);
			System.out.print("  ");
			System.out.print(count+3);
			System.out.print("  ");
			System.out.print(count+4);
			System.out.print("  ");
			System.out.print(count+5);
			System.out.println("    ");	
		}

	}

}
